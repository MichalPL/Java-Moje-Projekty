public class Weight {
    private int exp;
    private int set;
    private int item;
    private int leader;

    public void setExp(int exp)
    {
        this.exp = exp;
    }

    public void setSet(int set) {
        this.set = set;
    }

    public void setItem(int item) {
        this.item = item;
    }

    public void  setLeader(int leader) {
        this.leader = leader;
    }

    public int getExp() {
        return exp;
    }

    public int getSet() {
        return set;
    }

    public int getItem() {
        return item;
    }

    public int getLeader() {
        return leader;
    }

}
